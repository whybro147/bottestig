async def setup_bot(bot_config):
    client = TelegramClient(
        session=bot_config['session_name'],
        api_id=bot_config['api_id'],
        api_hash=bot_config['api_hash'],
        connection_retries=10,
        auto_reconnect=True,
        timeout=60,
        request_retries=5
    )
    
    bot_active_rants = {}
    bot_user_sessions = {}
    
    all_active_rants[bot_config['name']] = bot_active_rants
    all_user_sessions[bot_config['name']] = bot_user_sessions
    all_clients[bot_config['name']] = client
    
    @client.on(events.NewMessage(pattern=r'^(\.asr|\.stoprant|\.asrhelp|\.ping)'))
    async def handle_commands(event):
        if event.sender_id not in bot_config['authorized_users']:
            return
        
        try:
            command = event.text.split()[0]
            
            if command == '.ping':
                await handle_ping(event, bot_config['name'])
            elif command == '.asrhelp':
                await handle_help(event, bot_config['name'])
            elif command == '.stoprant':
                await handle_stop_rant(event, bot_config, bot_active_rants)
            elif command.startswith('.asr'):
                await start_asr_session(event, bot_config, bot_user_sessions)
        except Exception as e:
            pass
    
    @client.on(events.NewMessage)
    async def handle_messages(event):
        await handle_session_messages(event, bot_config, bot_user_sessions, bot_active_rants, client)
    
    max_retries = 10
    retry_delay = 30
    
    for attempt in range(max_retries):
        try:
            await client.start(bot_token=bot_config['bot_token'])
            me = await client.get_me()
            print(f"‚úÖ {bot_config['name']} started successfully!")
            print(f"   ü§ñ Username: @{me.username}")
            print(f"   üÜî ID: {me.id}")
            
            while True:
                await asyncio.sleep(300)
                try:
                    await client.get_me()
                except (ConnectionError, OSError):
                    print(f"‚ùå {bot_config['name']} connection lost, reconnecting...")
                    try:
                        await client.disconnect()
                    except:
                        pass
                    await asyncio.sleep(5)
                    await client.start(bot_token=bot_config['bot_token'])
                    print(f"‚úÖ {bot_config['name']} reconnected!")
                    
        except (ConnectionError, OSError) as e:
            print(f"‚ùå {bot_config['name']} connection error (attempt {attempt + 1}/{max_retries}): {e}")
            if attempt < max_retries - 1:
                print(f"‚è≥ {bot_config['name']} retrying in {retry_delay} seconds...")
                await asyncio.sleep(retry_delay)
                retry_delay *= 2
            else:
                print(f"‚ùå {bot_config['name']} max retries reached.")
                break
        except Exception as e:
            print(f"‚ùå {bot_config['name']} unexpected error: {e}")
            break
    
    await client.run_until_disconnected()

